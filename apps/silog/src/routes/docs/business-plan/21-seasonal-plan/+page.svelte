		<section class="content-block">
			<h2>21.5 Seasonal Cash Flow Management</h2>
			<p>Revenue variance creates cash flow challenges. Plan ahead to avoid cash shortages during low seasons.</p>

			<div class="cash-strategy">
				<div class="strategy-box">
					<h3>Save During Peak Seasons</h3>
					<p><strong>Rule: Set aside 15% of profit during Nov-Dec & Jun-Sep (high revenue months)</strong></p>
					<ul>
						<li>Nov-Dec: Average ₱30,000 profit/month → Save ₱4,500/month</li>
						<li>Jun-Sep: Average ₱20,000 profit/month → Save ₱3,000/month</li>
						<li>8-month reserve: ~₱60,000 emergency fund</li>
					</ul>
					<p class="note">This buffer covers low-season shortfalls & unexpected expenses.</p>
				</div>

				<div class="strategy-box">
					<h3>Adjust Staffing by Season</h3>
					<p>Don't run full crew during slow months. Save on labor:</p>
					<ul>
						<li><strong>Apr-May (Summer):</strong> 1-2 staff daytime, ramp night shift</li>
						<li><strong>Oct-31, Mar-15:</strong> Reduce daytime staff, focus night crew</li>
						<li><strong>Jun-Sep:</strong> Full operations (peak student season)</li>
						<li><strong>Nov-Dec:</strong> Add temp staff for holiday rush</li>
					</ul>
					<p class="note">Flexible staffing saves ₱20,000-30,000/month during low seasons.</p>
				</div>

				<div class="strategy-box">
					<h3>Manage Inventory by Season</h3>
					<p>Don't over-stock during low seasons:</p>
					<ul>
						<li><strong>Summer (Apr-May):</strong> Order only 60% normal inventory levels</li>
						<li><strong>Breaks:</strong> 70% normal levels</li>
						<li><strong>Peak (Jun-Dec):</strong> Full inventory + safety stock</li>
					</ul>
					<p class="note">Reduces spoilage losses & frees up cash for operations.</p>
				</div>

				<div class="strategy-box">
					<h3>Delivery Recession Insurance</h3>
					<p>Delivery orders help stabilize revenue during low seasons:</p>
					<ul>
						<li>Consistently expect +30-50% more orders during summer & breaks</li>
						<li>Budget FoodPanda marketing during slow seasons</li>
						<li>Delivery is the "shock absorber" for seasonal volatility</li>
					</ul>
					<p class="note">Without delivery, April revenue would drop to ₱40,000-45,000/month.</p>
				</div>
			</div>
