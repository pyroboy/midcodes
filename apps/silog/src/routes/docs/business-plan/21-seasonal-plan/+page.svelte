			<!-- Summer Break -->
			<div class="season-strategy">
				<div class="season-header summer">
					<h3>☀️ Summer Break (Apr-May)</h3>
					<p class="impact-badge">-60 to -70% Walk-In Impact</p>
				</div>

				<div class="strategy-grid">
					<div class="strategy-item">
						<h4>Family Bilao Packs</h4>
						<p>Create shareable meal packs for family gathering & reunions (very common during summer).</p>
						<ul>
							<li><strong>"Pamilya Pack":</strong> 4x silog + 1x lugaw (₱380 combo)</li>
							<li><strong>"Reunion Special":</strong> 10x assorted silog (₱900)</li>
							<li>Bilao packaging (reusable) for premium feel</li>
						</ul>
					</div>

					<div class="strategy-item">
						<h4>Delivery Push</h4>
						<p>Target BPO workers, office workers staying in city during summer. Increase FoodPanda marketing.</p>
						<ul>
							<li>Flash sales 3-5x/week on delivery platforms</li>
							<li>Target: 30-40 delivery orders/day (₱800-1,000 daily)</li>
							<li>Maintains ₱1,000-1,500 total daily revenue</li>
						</ul>
					</div>

					<div class="strategy-item">
						<h4>Extended Hours</h4>
						<p>Open earlier (5 AM) for morning fitness crowd at Grandstand Stadium. Capture breakfast market.</p>
						<ul>
							<li>5-7 AM shift: Light silog only, 1 staff</li>
							<li>Potential: ₱300-500 early morning sales</li>
						</ul>
					</div>

					<div class="strategy-item">
						<h4>Menu Adjustments</h4>
						<p>Emphasize comfort food & sharing meals.</p>
						<ul>
							<li>Feature "Unlimited Lugaw" 1-hour specials</li>
							<li>Create "Summer Light" menu (less heavy items)</li>
							<li>Promote refreshing beverages</li>
						</ul>
					</div>
				</div>

				<div class="revenue-projection summer">
					<h4>Summer Revenue Projection</h4>
					<p><strong>Apr-May (60 days):</strong></p>
					<ul style="padding-left: 1.5rem;">
						<li>Walk-in: ₱800/day × 60 = ₱48,000</li>
						<li>Delivery: ₱900/day × 60 = ₱54,000</li>
						<li>Family packs: ₱500/day × 60 = ₱30,000</li>
						<li><strong>Total: ₱132,000</strong> (vs ₱150,000 normal rate)</li>
					</ul>
