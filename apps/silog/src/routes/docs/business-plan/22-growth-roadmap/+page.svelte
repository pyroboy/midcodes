		<section class="content-block">
			<h2>22.7 Monthly KPI Dashboard (What to Track)</h2>
			<p>These metrics tell you if you're on track for each phase. Review monthly to stay aligned with roadmap targets.</p>

			<div class="kpi-categories">
				<div class="kpi-section">
					<h3>🔢 Sales & Revenue KPIs</h3>
					<ul class="kpi-list">
						<li><strong>Daily Covers (Walk-In):</strong> Count of customers per day. Goal: Track daily, average weekly.</li>
						<li><strong>Monthly Revenue (Walk-In):</strong> Total walk-in sales. Target: Phase 1: ₱57-142k, Phase 2: ₱150-225k, etc.</li>
						<li><strong>Monthly Revenue (Delivery):</strong> FoodPanda + GrabFood combined. Target: 10-20% of total revenue by Month 6.</li>
						<li><strong>Average Ticket:</strong> Revenue ÷ covers. Should stay ₱90-110 (walk-in). Track if pricing is working.</li>
						<li><strong>Revenue per Sq Meter:</strong> Sales ÷ floor space. Efficiency metric. Compare to fast-casual benchmarks.</li>
					</ul>
				</div>

				<div class="kpi-section">
					<h3>💰 Profitability KPIs</h3>
					<ul class="kpi-list">
						<li><strong>COGS %:</strong> Cost of goods ÷ revenue. Target: 35-40%. If rising, menu mix or supplier costs are off.</li>
						<li><strong>Gross Profit Margin:</strong> (Revenue - COGS) ÷ Revenue. Target: 60%+ for walk-in, 25%+ for delivery.</li>
						<li><strong>Net Profit Margin:</strong> Net income ÷ revenue. Target: Phase 1: 15%, Phase 2: 25%, Phase 3: 30%+.</li>
						<li><strong>Labor Cost %:</strong> Payroll ÷ revenue. Target: 12-15%. Watch for creep as headcount grows.</li>
						<li><strong>Rent % (rent expense)</strong>: Rent ÷ revenue. Target: <5% (we own building = not applicable initially).</li>
					</ul>
				</div>

				<div class="kpi-section">
					<h3>📊 Operations KPIs</h3>
					<ul class="kpi-list">
						<li><strong>FoodPanda Star Rating:</strong> Monitor 4.5+ target. Track complaints & address immediately.</li>
						<li><strong>Average Delivery Order Value:</strong> FoodPanda revenue ÷ delivery orders. Target: ₱120-150.</li>
						<li><strong>Delivery Orders/Day:</strong> Count of daily orders on FoodPanda. Target: Phase 1: 5-8, Phase 2: 15-20, Phase 3: 30-40.</li>
						<li><strong>Food Cost per Item:</strong> COGS breakdown by menu item. Identify high-cost items for margin optimization.</li>
						<li><strong>Waste/Spoilage %:</strong> Track daily waste as % of inventory received. Target: <3%. High waste signals over-prepping.</li>
					</ul>
