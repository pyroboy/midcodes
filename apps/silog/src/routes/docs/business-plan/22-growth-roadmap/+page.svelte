		<section class="content-block">
			<h2>22.1 Strategic Growth Framework</h2>
			<p>Our growth roadmap is divided into four phases over 18 months. Each phase has specific revenue targets, operational milestones, and decision triggers that tell us when to escalate to the next phase.</p>

			<div class="framework-intro">
				<p>Philosophy: <strong>Stabilize before scaling.</strong> We don't scale delivery until walk-in is stable. We don't expand hours until current hours are optimized. This prevents operational chaos.</p>
