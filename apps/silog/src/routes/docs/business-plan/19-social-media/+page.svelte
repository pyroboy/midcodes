		<section class="content-block">
			<h2>19.3 Signature Content Ideas</h2>
			<p>These are proven content formats that perform well for food businesses. Film these during first week to build content bank.</p>

			<div class="content-ideas">
				<div class="idea-item">
					<h4>🔥 Sizzle Shots</h4>
					<p>15-30 second video of tapa or meat hitting hot pan. Sound design critical (ASMR sizzle). Post with: "Fresh off the grill 🔥" captions. High engagement & shares.</p>
				</div>
				<div class="idea-item">
					<h4>🥚 Egg Crack & Cook</h4>
					<p>Satisfying close-up of cracking egg, spreading yolk, watching it cook. Time-lapse 3-5 seconds. Very shareable on all platforms, especially TikTok. "Perfect runny yolk every time."</p>
				</div>
				<div class="idea-item">
					<h4>🍚 Rice Plating Technique</h4>
					<p>Show rice mounding technique with satisfaction appeal. Demonstrates care & quality. "This is why our rice is different" angle. Educational + aspirational.</p>
				</div>
				<div class="idea-item">
					<h4>🌙 Late Night Cooking</h4>
					<p>For night shift content: Behind-the-scenes of kitchen during night hours. "Feeding Tagbilaran's night owls" narrative. Shows dedication & availability. Perfect for BPO workers.</p>
				</div>
				<div class="idea-item">
					<h4>👨‍🍳 Owner Testimonial</h4>
					<p>"Why we opened Kuya's" 30-60 second video explaining passion & quality commitment. Builds founder credibility. First video to post pre-launch.</p>
				</div>
				<div class="idea-item">
					<h4>📱 "How Did You Hear?" Stickers</h4>
					<p>TikTok & Instagram Reels: Ask followers how they discovered us. Polls & quizzes generate engagement & valuable feedback. Track which channels drive discovery.</p>
				</div>
				<div class="idea-item">
					<h4>🎤 Customer Testimonial Series</h4>
					<p>Quick interview clips: "What's your favorite order?" or "Why you keep coming back?" Authentic social proof. Request permission & tag customer accounts.</p>
				</div>
				<div class="idea-item">
					<h4>⏰ "Come Try Us" Countdown</h4>
					<p>14-day pre-launch calendar: "4 days until Kuya's opens!" Build hype daily. Show construction progress, menu teases, team intros. Create anticipation.</p>
				</div>
			</div>
