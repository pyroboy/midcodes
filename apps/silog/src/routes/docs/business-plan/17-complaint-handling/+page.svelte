		<h2>üò° 17.1 The "LATTE" Method</h2>
		<p class="section-intro">Lima ka steps para ma-handle ang bisan unsang complaint.</p>

		<div class="latte-grid">
			<div class="latte-card">
				<div class="letter">L</div>
				<h4>LISTEN</h4>
				<p>Hunong sa imong gibuhat. Tan-aw nila. Pabuhata sila'g sulti.</p>
				<div class="script-box">
					<span class="script">"Oo sir/ma'am, unsa may problema?"</span>
				</div>
			</div>

			<div class="latte-card">
				<div class="letter">A</div>
				<h4>ACKNOWLEDGE</h4>
				<p>I-acknowledge ang ilang feeling. Ayaw'g defensive.</p>
				<div class="script-box">
					<span class="script">"Sige sir/ma'am, noted. Sorry gyud kaayo."</span>
				</div>
			</div>

			<div class="latte-card">
				<div class="letter">T</div>
				<h4>TAKE ACTION</h4>
				<p>Solve dayon. Ayaw'g hulat. Priority status.</p>
				<div class="script-box">
					<span class="script">"Sige sir/ma'am, atimanon nako dayon ni."</span>
				</div>
			</div>

			<div class="latte-card">
				<div class="letter">T</div>
				<h4>THANK</h4>
				<p>Pasalamati sila kay gi-report nila ang issue.</p>
				<div class="script-box">
					<span class="script">"Salamat sa pagsulti sir/ma'am."</span>
				</div>
			</div>

			<div class="latte-card">
				<div class="letter">E</div>
				<h4>EXPLAIN</h4>
				<p>Brief explanation lang. Ayaw'g excuses.</p>
				<div class="script-box">
					<span class="script">"Nagka-busy mi ganina, mao natong nahitabo. Dili na gyud mausab."</span>
				</div>
			</div>
		</div>

		<div class="divider"></div>

		<h2>üó£Ô∏è 17.2 Common Scripts (Bisaya)</h2>

		<div class="scenario-card">
			<div class="scenario-header">
				<span class="scenario-num">1</span>
				<h4>"Bugnaw/hilaw ang pagkaon!" (Food is cold/raw)</h4>
			</div>
			<div class="scenario-body">
				<div class="staff-response">
					<span class="label">Staff Script:</span>
					<span class="script">"Hala, bugnaw diay? Sorry kaayo Ma'am/Sir. Ilisan nato ni ron dayon."</span>
				</div>
				<div class="action-box">
					<strong>Action:</strong> Kuhaa ang plato. Ilabay. Luto'g bag-o. Priority status.
				</div>
			</div>
		</div>

		<div class="scenario-card">
			<div class="scenario-header">
				<span class="scenario-num">2</span>
				<h4>"Dugay kaayo!" (It's taking too long)</h4>
			</div>
			<div class="scenario-body">
				<div class="staff-response">
					<span class="label">Staff Script #1:</span>
					<span class="script">"Ay sorry Sir, i-follow up nako sa kitchen ron."</span>
				</div>
				<p class="action-note">I-check ang ticket, then balik:</p>
				<div class="staff-response">
					<span class="label">Staff Script #2:</span>
					<span class="script">"Wait lang Sir ha, gi-plating na to. 2 minutes na lang."</span>
				</div>
				<div class="warning-note">
					‚ö†Ô∏è <strong>AYAW'G INGON "Hapit na"</strong> - say specific time like "2 minutes"
				</div>
			</div>
		</div>

		<div class="scenario-card">
			<div class="scenario-header">
				<span class="scenario-num">3</span>
				<h4>"Naay buhok!" (There's a hair)</h4>
			</div>
			<div class="scenario-body">
				<div class="staff-response">
					<span class="label">Staff Script:</span>
					<span class="script">"Hala, sorry jud kaayo Ma'am/Sir!"</span>
				</div>
				<div class="action-box">
					<strong>Action:</strong> Kuhaa dayon ang plato. AYAW'G TAN-AW OG MAAYO kung tinuod ba. Just remove it.
				</div>
				<div class="staff-response">
					<span class="label">Follow-up:</span>
					<span class="script">"Ako lang ning ilisan Ma'am, or gusto mo i-refund nalang?"</span>
				</div>
			</div>
		</div>

		<div class="scenario-card">
			<div class="scenario-header">
				<span class="scenario-num">4</span>
				<h4>"Dili ni akong order!" (Wrong order)</h4>
			</div>
			<div class="scenario-body">
				<div class="staff-response">
					<span class="label">Staff Script:</span>
					<span class="script">"Ay sorry Ma'am, unsa gani to inyong order? Ako i-double check."</span>
				</div>
				<div class="action-box">
					<strong>Action:</strong> Verify the ticket. If mali gyud, ilisan. If sakto ang ticket, explain politely then offer replacement kung gusto nila.
				</div>
			</div>
		</div>

		<div class="scenario-card">
			<div class="scenario-header">
				<span class="scenario-num">5</span>
				<h4>"Kulang ang sukli!" (Short change)</h4>
			</div>
			<div class="scenario-body">
				<div class="staff-response">
					<span class="label">Staff Script:</span>
					<span class="script">"Sige sir/ma'am, i-check nako ang cash register."</span>
				</div>
				<div class="action-box">
					<strong>Action:</strong> Recount carefully. If naa'y sayop, apologize and give the correct change immediately.
				</div>
				<div class="staff-response">
					<span class="label">If confirmed:</span>
					<span class="script">"Pasensya gyud sir/ma'am, sayop gyud. Mao ni ang husto."</span>
				</div>
			</div>
		</div>

		<div class="divider"></div>

		<h2>üö® 17.3 Escalation Protocol</h2>
		<p class="section-intro">Kung dili na ma-handle sa staff ang situation.</p>

		<div class="escalation-steps">
			<div class="step">
				<span class="num">1</span>
				<div class="content">
					<h4>Stay Calm</h4>
					<p>Ayaw'g sumpaki. Ayaw'g pataasin ang voice.</p>
				</div>
			</div>
			<div class="step">
				<span class="num">2</span>
				<div class="content">
					<h4>Call the Manager/Owner</h4>
					<p>Ingna: <em>"Sir/Ma'am, tawagon nako ang akong manager para makatabang."</em></p>
				</div>
			</div>
			<div class="step">
				<span class="num">3</span>
				<div class="content">
					<h4>Document</h4>
					<p>After ma-resolve, isulat ang incident sa logbook.</p>
				</div>
			</div>
		</div>

