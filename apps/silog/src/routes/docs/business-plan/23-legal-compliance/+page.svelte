			<h2>23.2 Annual Permit Renewal Schedule</h2>
			<p>All food business permits in the Philippines are annual. Mark these renewal dates on the calendar and budget for application/renewal costs.</p>

			<div class="permit-schedule">
				<div class="permit-item">
					<div class="permit-header">
						<h3>DTI Registration</h3>
						<span class="renewal-cycle">Annual: _____ (Set your start date)</span>
					</div>
					<div class="permit-details">
						<p><strong>What it is:</strong> Department of Trade & Industry business registration. Registrar of single proprietorships.</p>
						<p><strong>Cost:</strong> ₱1,000-1,500</p>
						<p><strong>Where:</strong> DTI Regional Office, Tagbilaran or online via DTI online registry</p>
						<p><strong>Requirements:</strong> Company name, owner details, business address, detailed business description</p>
						<p><strong>Timeline:</strong> 3-5 working days after submission</p>
						<p><strong>Renewal process:</strong> Submit at your anniversary date each year. Same requirements.</p>
						<p class="note"><strong>Pro tip:</strong> Get physical certificate + digital copy. Post digital copy in kitchen for health inspector reference.</p>
					</div>
				</div>

				<div class="permit-item">
					<div class="permit-header">
						<h3>Mayor's Permit (Business License)</h3>
						<span class="renewal-cycle">Annual: _____ (Usually Jan-Mar)</span>
					</div>
					<div class="permit-details">
						<p><strong>What it is:</strong> Municipal license to operate a business in Tagbilaran City. Required to operate legally.</p>
						<p><strong>Cost:</strong> ₱6,000-8,000 (depends on assessed business classification & earnings)</p>
						<p><strong>Where:</strong> City Treasurer's Office, Tagbilaran City Hall</p>
						<p><strong>Requirements:</strong> Proof of business (DTI cert), proof of location (barangay clearance, ownership cert), tax identification number (TIN)</p>
						<p><strong>Timeline:</strong> 7-10 working days after submission</p>
						<p><strong>Renewal process:</strong> Submit renewal application by Jan 31 of each year to avoid late penalties.</p>
						<p class="note"><strong>Pro tip:</strong> Renew early (Dec-Jan) before rush period. Keep 2 copies—one for office, one for kitchen public display.</p>
					</div>
				</div>

				<div class="permit-item">
					<div class="permit-header">
						<h3>Sanitary Permit (Health Certificate)</h3>
						<span class="renewal-cycle">Annual: _____ (Match DTI date if possible)</span>
					</div>
					<div class="permit-details">
						<p><strong>What it is:</strong> City Health Office certification that kitchen meets sanitation & food safety standards. Required for food businesses.</p>
						<p><strong>Cost:</strong> ₱2,500-4,000 (includes staff health clearances)</p>
						<p><strong>Where:</strong> City Health Office (CHO), Tagbilaran or satellite clinic</p>
						<p><strong>Requirements:</strong>
							<ul style="padding-left: 1.5rem; margin: 0.5rem 0;">
								<li>Staff medical certs (chest x-ray, blood test) - each staff: ₱500-800</li>
								<li>Kitchen layout diagram</li>
								<li>Food handling training cert (can be online, ₱0-500)</li>
								<li>Proof of business (Mayor's permit, DTI)</li>
							</ul>
						</p>
						<p><strong>Inspection:</strong> CHO inspector visits kitchen. Must pass basic standards (clean equipment, proper storage, certified staff).</p>
						<p><strong>Timeline:</strong> 5-10 days after inspection (if passed)</p>
						<p class="note"><strong>Pro tip:</strong> Schedule inspection when kitchen is pristine. Inspector looks for: clean surfaces, proper food storage temp, certified staff, no cross-contamination risks.</p>
					</div>
				</div>

				<div class="permit-item">
					<div class="permit-header">
						<h3>BIR Registration (TIN Certificate)</h3>
						<span class="renewal-cycle">One-time registration (TIN is lifetime)</span>
					</div>
					<div class="permit-details">
						<p><strong>What it is:</strong> Bureau of Internal Revenue Tax Identification Number. Required to pay taxes & issue receipts.</p>
						<p><strong>Cost:</strong> ₱0 (free registration)</p>
						<p><strong>Where:</strong> BIR Regional Office, Cebu City or Tagbilaran satellite office (if available)</p>
						<p><strong>Requirements:</strong> Valid ID, proof of business (Mayor's permit, DTI cert), business address</p>
						<p><strong>Timeline:</strong> Same-day or 1-2 days</p>
						<p><strong>Important:</strong> Get BIR Registration Form (Form 1902-D) & Certificate (BIR Certificate). Display in restaurant. Use TIN on all receipts & documentation.</p>
						<p class="note"><strong>Pro tip:</strong> Register as VAT (Value Added Tax) if annual revenue > ₱3M. Otherwise, register as Non-VAT. Consult accountant on best option.</p>
					</div>
				</div>

