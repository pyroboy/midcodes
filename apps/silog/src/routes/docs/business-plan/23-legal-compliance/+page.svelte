			<h2>23.7 Insurance Considerations (Optional but Recommended)</h2>
			<p>While not legally mandated for small food businesses, insurance protects against unexpected catastrophic losses.</p>

			<div class="insurance-types">
				<div class="insurance-card">
					<h4>1. General Liability Insurance</h4>
					<p><strong>Covers:</strong> Customer gets injured (food poisoning, slip & fall), customer sues for damages</p>
					<p><strong>Cost:</strong> ₱8,000-12,000/year</p>
					<p><strong>Claim example:</strong> Customer claims food poisoning. Medical bills + lawsuit damages covered by insurer.</p>
					<p><strong>Recommendation:</strong> Highly recommended. Medical cost + legal defense can exceed ₱500k. Insurance is cheap protection.</p>
				</div>

				<div class="insurance-card">
					<h4>2. Property Insurance</h4>
					<p><strong>Covers:</strong> Kitchen equipment, inventory, building damage from fire/theft/natural disaster</p>
					<p><strong>Cost:</strong> ₱15,000-25,000/year (depends on assets covered)</p>
					<p><strong>Claim example:</strong> Fire destroys kitchen equipment. Insurance reimburses replacement cost.</p>
					<p><strong>Recommendation:</strong> Lower priority since we own building. But equipment replacement cost is ₱30k+. Good to have.</p>
				</div>

				<div class="insurance-card">
					<h4>3. Product Liability Insurance</h4>
					<p><strong>Covers:</strong> Defective food causes customer injury (allergen not declared, contamination, etc.)</p>
					<p><strong>Cost:</strong> Often bundled with general liability (₱8-12k total)</p>
					<p><strong>Claim example:</strong> Undisclosed peanut allergen in food. Customer has allergic reaction. Covered.</p>
					<p><strong>Recommendation:</strong> Essential if targeting customers with known allergies. Included in general liability usually.</p>
				</div>

				<div class="insurance-card">
					<h4>4. Workers Compensation Insurance</h4>
					<p><strong>Covers:</strong> Staff injury on the job (burns, cuts, slips). Medical bills + lost wages during recovery</p>
					<p><strong>Cost:</strong> ₱5,000-8,000/year (per employee)</p>
					<p><strong>Claim example:</strong> Staff gets 3rd-degree burn from hot oil. Insurance covers medical + 1 month lost wages.</p>
					<p><strong>Recommendation:</strong> Legally required if have employees in some cases. Check with SSS/labor dept. At minimum, ensure staff has SSS (which covers some benefits).</p>
				</div>
			</div>

			<div class="insurance-action">
				<h3>Insurance Action Plan</h3>
				<p><strong>Priority 1 (Month 1-2):</strong> Get general liability + product liability bundled policy. Cost ₱8-12k/year. Peace of mind for customer-facing risk.</p>
				<p><strong>Priority 2 (Month 3+):</strong> If business growing, add property insurance. Equipment replacement is expensive.</p>
				<p><strong>Priority 3:</strong> Ensure all staff enrolled in SSS. Provides basic workers compensation coverage.</p>
				<p><strong>Where to buy:</strong> Insurance brokers in Tagbilaran (Insular Life, Prudential, AXA) specialize in small business policies. Get quotes from 2-3 providers. Compare coverage & deductibles.</p>
