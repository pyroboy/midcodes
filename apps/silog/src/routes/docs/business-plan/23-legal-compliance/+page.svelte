		<section class="content-block">
			<h2>23.3 BMBE Certification (Barangay Micro Business Enterprise)</h2>
			<p>Kuya's qualifies for BMBE status, which offers significant tax & operational benefits. Highly recommended to pursue.</p>

			<div class="bmbe-section">
				<div class="bmbe-box">
					<h3>What is BMBE?</h3>
					<p>Government program supporting small barangay-based businesses with < 3M annual revenue. Kuya's qualifies.</p>
				</div>

				<div class="bmbe-benefits">
					<h3>🎁 BMBE Benefits</h3>
					<div class="benefit-cards">
						<div class="benefit-card">
							<h4>Tax Exemption</h4>
							<p>Exempted from income tax for 3 years (or until revenue exceeds ₱3M). Huge savings: ~₱100-150k/year in taxes.</p>
						</div>
						<div class="benefit-card">
							<h4>Simplified Reporting</h4>
							<p>Instead of complex quarterly/monthly BIR filings, submit annual tax return only. Less accounting overhead.</p>
						</div>
						<div class="benefit-card">
							<h4>SSS & Health Insurance Subsidy</h4>
							<p>Government may subsidize staff SSS & PhilHealth contributions. Check with barangay for current programs.</p>
						</div>
						<div class="benefit-card">
							<h4>Priority for Loans</h4>
							<p>Banks & microfinance institutions offer better terms to BMBE-certified businesses. Good for future expansion capital.</p>
						</div>
						<div class="benefit-card">
							<h4>Barangay Support</h4>
							<p>Access to barangay business development programs, training, networking events.</p>
						</div>
						<div class="benefit-card">
							<h4>No VAT Registration Requirement</h4>
							<p>Stay off VAT until revenue > ₱3M. Simplifies accounting significantly.</p>
						</div>
					</div>
				</div>

				<div class="bmbe-requirements">
					<h3>BMBE Requirements & Process</h3>
					<ul>
						<li><strong>Annual Revenue:</strong> ₱0 - ₱3,000,000</li>
						<li><strong>Location:</strong> Registered in barangay (Maria Clara St., Tabunok, Tagbilaran = qualifies)</li>
						<li><strong>Employees:</strong> No more than 30 full-time staff (we'll have 2-4 initially)</li>
						<li><strong>Assets:</strong> ≤ ₱4M in total assets</li>
					</ul>
					<p><strong>Application Process:</strong></p>
					<ol style="padding-left: 1.5rem;">
						<li>Contact Barangay Hall BMBE Desk (Barangay Tabunok)</li>
						<li>Submit application form + supporting docs:
							<ul style="padding-left: 1.5rem; margin-top: 0.5rem;">
								<li>Barangay Clearance</li>
								<li>DTI Registration Certificate</li>
								<li>Mayor's Permit</li>
								<li>ID (owner)</li>
								<li>Business location photo</li>
							</ul>
						</li>
						<li>Barangay processes (2-3 weeks typical)</li>
						<li>Receive BMBE Certificate (valid for 5 years, renewable)</li>
						<li>Register with BIR as BMBE entity (takes 1-2 days)</li>
					</ol>
					<p><strong>Cost:</strong> ₱0-500 (mostly administrative fees, varies by barangay)</p>
				</div>

				<div class="bmbe-action">
					<h3>Recommendation</h3>
					<div class="action-box">
						<p><strong>Apply for BMBE immediately after business launch (Month 1-2).</strong></p>
						<p>Tax exemption alone saves ~₱100k+ over 3 years. Plus simplified accounting. No downside—you'll still be required to do basic record-keeping, but filing is easier.</p>
						<p>Timeline: Start application by Month 2. Should be approved by Month 3-4. Retroactive tax exemption from business start date if approved within first year.</p>
					</div>
				</div>
			</div>
