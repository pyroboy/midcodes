		<section class="content-block">
			<h2>23.4 Health & Safety Compliance Checklist</h2>
			<p>The City Health Office (CHO) inspects food businesses regularly. Stay inspection-ready with this checklist.</p>

			<div class="compliance-checklist">
				<div class="checklist-section">
					<h3>✅ Kitchen Standards</h3>
					<ul class="check-list">
						<li>Floors: Clean, non-slip, no food debris or grease</li>
						<li>Walls: Smooth, washable, free from mold & stains</li>
						<li>Ceiling: Clean, no water damage or peeling paint</li>
						<li>Ventilation: Grease hood above cooking area (extract air outdoors)</li>
						<li>Lighting: ≥ 50 lux brightness (bright enough to see food clearly)</li>
						<li>Handwashing: Separate sink with hot water, soap, paper towels (not cloth)</li>
						<li>Refrigeration: Working thermometer showing <4°C. Daily temp log.</li>
						<li>Freezer: Working, displaying <-18°C</li>
					</ul>
				</div>

				<div class="checklist-section">
					<h3>✅ Food Storage & Handling</h3>
					<ul class="check-list">
						<li>Raw & cooked food separated (different shelves/containers)</li>
						<li>Raw meat on bottom shelf (prevents drips onto other food)</li>
						<li>Eggs stored below 4°C (not at room temp)</li>
						<li>Vegetables stored separately from meat</li>
						<li>All food labeled with date prepped</li>
						<li>No unlabeled leftovers. Clear discard policy (max 4 hours room temp, max 3 days fridge)</li>
						<li>Dry goods in sealed containers (prevents pest entry)</li>
						<li>No personal items in kitchen (coats, bags, phones)</li>
					</ul>
				</div>

				<div class="checklist-section">
					<h3>✅ Staff & Hygiene</h3>
					<ul class="check-list">
						<li>All staff have current health certificate (chest x-ray + blood test)</li>
						<li>Health certificates prominently posted</li>
						<li>Staff training: Food handling & sanitation (cert on file)</li>
						<li>Hair restraints worn by all kitchen staff</li>
						<li>No eating/drinking/smoking in kitchen</li>
						<li>Staff wash hands after bathroom, between tasks</li>
						<li>Fingernails short, clean. No nail polish in kitchen.</li>
						<li>No staff with open cuts/wounds working food prep</li>
					</ul>
				</div>

				<div class="checklist-section">
					<h3>✅ Equipment & Tools</h3>
					<ul class="check-list">
						<li>All equipment clean & in working order</li>
						<li>Knives sanitized between use (bleach solution or dishwasher)</li>
						<li>Cutting boards: Separate for raw meat (red) vs vegetables (green)</li>
						<li>Thermometer for checking internal food temps (165°F for meat)</li>
						<li>No chipped dishes/containers (food contact surfaces must be intact)</li>
						<li>Cleaning supplies stored away from food (separate cabinet)</li>
						<li>No pest infestation (traps, baits visible = problem)</li>
					</ul>
				</div>

				<div class="checklist-section">
					<h3>✅ Documentation & Records</h3>
					<ul class="check-list">
						<li>Permits posted: DTI, Mayor's, Sanitary, BMBE, BIR</li>
						<li>Staff health certificates current (not expired)</li>
						<li>Food safety training certs on file</li>
						<li>Refrigerator temp log (daily check, manual or thermometer)</li>
						<li>Cleaning log (daily kitchen deep clean documented)</li>
						<li>Pest control log (if contractor visits)</li>
						<li>Complaint log (if customer complains, document & action taken)</li>
					</ul>
				</div>
			</div>

			<div class="pre-inspection-prep">
				<h3>Before a Health Inspection</h3>
				<p>CHO inspectors can show up unannounced. Always maintain these standards. But if you get notice (often 2-3 days), here's the prep:</p>
				<ol style="padding-left: 1.5rem;">
					<li><strong>Deep clean:</strong> 2 days before. Scrub everything (walls, floor, equipment). Clean grease hood thoroughly.</li>
					<li><strong>Verify documentation:</strong> Confirm all staff certs are current. Post visibly. Have spare copies ready.</li>
					<li><strong>Stock inspection:</strong> Verify all food is labeled & properly stored. Remove any questionable items.</li>
					<li><strong>Equipment check:</strong> Test all appliances. Ensure thermometer works & reads correct temp.</li>
					<li><strong>Staff briefing:</strong> Remind team not to volunteer info. Answer direct questions only. Be polite & professional.</li>
					<li><strong>Orient inspector:</strong> Show kitchen layout, introduce staff, offer tour. Cooperativeness matters.</li>
				</ol>
			</div>
