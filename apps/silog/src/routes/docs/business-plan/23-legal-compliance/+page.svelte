						<h3>Books of Accounts (BIR Registration)</h3>
						<span class="renewal-cycle">Annual: Register format at BIR</span>
					</div>
					<div class="permit-details">
						<p><strong>What it is:</strong> Official accounting records for tax compliance. BIR mandates specific format & audit trail.</p>
						<p><strong>Cost:</strong> ₱500-1,500 (per accountant fee for setup)</p>
						<p><strong>What to maintain:</strong>
