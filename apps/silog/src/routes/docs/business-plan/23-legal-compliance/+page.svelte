			<h2>23.6 Record Keeping Requirements</h2>
			<p>Business & food safety regulations require you to maintain certain records. Keep originals safe & digitally backed up.</p>

			<div class="records-grid">
				<div class="record-category">
					<h4>Sales Records</h4>
					<ul>
						<li><strong>Cash Register:</strong> Daily sales tape (receipt roll). Keep for 3 years minimum.</li>
						<li><strong>Sales Journal:</strong> Daily summary (walk-in revenue, delivery revenue, by payment method). Monthly totals.</li>
						<li><strong>Customer Receipts:</strong> Issue numbered receipts for all transactions (required by BIR if registered as business).</li>
						<li><strong>Refund Records:</strong> Document any refunds/voids (reason, amount, staff name).</li>
					</ul>
				</div>

				<div class="record-category">
					<h4>Expense Records</h4>
					<ul>
						<li><strong>Supplier Invoices:</strong> Keep all receipts from food suppliers, utilities, staff payroll. File chronologically. 3-year retention.</li>
						<li><strong>Payroll Records:</strong> Monthly staff payroll (names, hours, rates, deductions). Required by BIR & labor department.</li>
						<li><strong>Utility Bills:</strong> Water, electricity, gas. Track monthly. Useful for budgeting.</li>
						<li><strong>Maintenance/Repair Receipts:</strong> Equipment repairs, cleaning supplies. Track separately for tax deductions.</li>
					</ul>
				</div>

				<div class="record-category">
					<h4>Compliance Records</h4>
					<ul>
						<li><strong>Permits & Licenses:</strong> Digital copies of all current permits (DTI, Mayor's, Sanitary, BIR, BMBE, Fire Safety). Post originals in kitchen.</li>
						<li><strong>Staff Health Certificates:</strong> Current health clearances for all employees. BIR may request during audit.</li>
						<li><strong>Food Safety Training:</strong> Training certificates for staff. Proof of compliance if CHO inspects.</li>
						<li><strong>Inspection Reports:</strong> CHO & BFP inspection reports (keep even if passed). Evidence of compliance history.</li>
					</ul>
				</div>

				<div class="record-category">
					<h4>Safety & Operations Logs</h4>
					<ul>
						<li><strong>Refrigerator Temperature Log:</strong> Daily fridge/freezer temps. Demonstrates food safety awareness. CHO appreciates this.</li>
						<li><strong>Cleaning Log:</strong> Daily kitchen deep clean. Who cleaned, when, signature. Proves hygiene standards.</li>
						<li><strong>Complaint Log:</strong> Any customer complaints (food quality, service, etc.). Action taken. Proof of customer care.</li>
						<li><strong>Pest Control Log:</strong> If contractor treats for pests, document visit & treatment. Proof of pest management.</li>
					</ul>
				</div>
			</div>

			<div class="record-storage">
				<h3>Record Storage Best Practices</h3>
				<div class="storage-box">
					<h4>Physical Storage</h4>
					<ul>
						<li>Keep originals in waterproof box or filing cabinet in office</li>
						<li>Organize by year, then by month</li>
						<li>Keep 3 years minimum (tax requirement)</li>
						<li>Original health certs, permits should be posted publicly (kitchen wall)</li>
					</ul>
