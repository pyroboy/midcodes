						<li><strong>3 years minimum:</strong> Sales receipts, expense invoices, payroll, bank statements</li>
						<li><strong>5 years minimum:</strong> Tax returns (ITR, annual BIR filings)</li>
						<li><strong>1 year minimum:</strong> Health certificates (but renew before expiration)</li>
