								<li>Bank reconciliation (match business bank to cash records)</li>
							</ul>
						</p>
						<p><strong>Filing frequency:</strong> Annual. Submit Income Tax Return (ITR) every April 15.</p>
						<p><strong>Penalties:</strong> Missing or falsified books can result in ₱10,000+ fines & potential criminal charges.</p>
						<p class="note"><strong>Pro tip:</strong> Use simple spreadsheet (Google Sheets or Excel) or POS system with integrated accounting. Review monthly with accountant. Stay ahead of tax season.</p>
