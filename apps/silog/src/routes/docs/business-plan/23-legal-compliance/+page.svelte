		<section class="content-block">
			<h2>23.5 Fire Safety Compliance</h2>
			<p>Food establishments require annual fire safety inspection by Bureau of Fire Protection (BFP). Non-compliance can result in closure orders.</p>

			<div class="fire-safety-items">
				<div class="fire-item">
					<h4>Fire Safety Requirements</h4>
					<ul>
						<li><strong>Fire Extinguishers:</strong> Minimum 2 units (ABC-type for kitchen). One near kitchen, one near entrance. Pressure gauge must show "green zone". Annual inspection/refill required (₱500-800/unit).</li>
						<li><strong>Exit Signs:</strong> Illuminated "EXIT" signs pointing to emergency exit. Must be visible even if lights fail.</li>
						<li><strong>Fire Alarm System:</strong> Not required for small establishments <100 sqm. But smoke detector is recommended (₱2,000-3,000).</li>
						<li><strong>Emergency Exit:</strong> At least one clear exit (not blocked by furniture or inventory). Width ≥ 0.75m.</li>
						<li><strong>Fire Escape Ladder:</strong> If upstairs location. Must be tested & certified annually.</li>
						<li><strong>Fire Safety Inspection Certificate (BFP):</strong> Annual inspection by Bureau of Fire Protection. Pass inspection to get certificate.</li>
					</ul>
				</div>

				<div class="fire-inspection">
					<h4>BFP Annual Inspection Process</h4>
					<ol style="padding-left: 1.5rem;">
						<li>Contact local BFP station (Tagbilaran Fire Station)</li>
						<li>Request fire safety inspection (typically free or ₱500 fee)</li>
						<li>Inspector checks: extinguishers, exits, electrical systems, grease hood</li>
						<li>If passed: Receive Fire Safety Inspection Certificate (valid 1 year)</li>
						<li>If failed: Get written citation with corrective actions. Schedule re-inspection after fixing issues.</li>
					</ol>
					<p><strong>Timeline:</strong> Inspection usually same week as request. Certificate issued on-the-spot if passed. Keep copy posted in kitchen.</p>
				</div>

				<div class="fire-action">
					<h3>Action Items</h3>
					<ul>
						<li>Month 1: Purchase 2 fire extinguishers (ABC-type, 4kg minimum). Install one in kitchen, one near door.</li>
						<li>Month 1-2: Contact BFP for inspection. Submit inspection request before Day 1 operations if possible.</li>
						<li>Month 1: Install illuminated "EXIT" sign above main door.</li>
						<li>Ongoing: Check fire extinguisher pressure gauge monthly. If drops below green zone, refill immediately.</li>
						<li>Annual: Arrange BFP re-inspection before certificate expires.</li>
					</ul>
				</div>
			</div>
