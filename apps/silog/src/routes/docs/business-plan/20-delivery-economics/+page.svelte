			<h2>20.8 Delivery Operations Challenges</h2>
			<p>Delivery adds operational complexity. Plan for these challenges:</p>

			<div class="challenges-grid">
				<div class="challenge-card">
					<h4>🕐 Timing Management</h4>
					<p><strong>Challenge:</strong> FoodPanda orders arrive unpredictably, disrupting walk-in rhythm</p>
					<p><strong>Solution:</strong> Dedicated delivery prep person in Month 3+. Build delivery orders during slow walk-in times (2-4 PM).</p>
				</div>

				<div class="challenge-card">
					<h4>📦 Packaging & Storage</h4>
					<p><strong>Challenge:</strong> Extra inventory of containers takes space</p>
					<p><strong>Solution:</strong> Store packaging in compact wall shelving above kitchen. Reorder bi-weekly in bulk.</p>
				</div>

				<div class="challenge-card">
					<h4>⭐ Quality Control</h4>
					<p><strong>Challenge:</strong> Delivery food arrives 20-30 minutes later. Hot items cool. Quality suffers.</p>
					<p><strong>Solution:</strong> Isolate hot items in insulated containers. Train packing for heat retention. Accept that delivery food won't be as hot as dine-in (manage expectations in photos).</p>
				</div>

				<div class="challenge-card">
					<h4>😤 Bad Ratings</h4>
					<p><strong>Challenge:</strong> Even one late or cold order tanks rating</p>
					<p><strong>Solution:</strong> Set aggressive delivery time expectations (30-35 min). Monitor orders. Respond to negative reviews professionally & offer discount on next order.</p>
				</div>

				<div class="challenge-card">
					<h4>💰 Slow Payouts</h4>
					<p><strong>Challenge:</strong> FoodPanda pays weekly, 7 days after delivery. Cash flow impact</p>
					<p><strong>Solution:</strong> Budget for weekly accounts receivable. Don't depend on delivery cash to cover daily opex.</p>
				</div>

				<div class="challenge-card">
					<h4>📞 Customer Support</h4>
					<p><strong>Challenge:</strong> Delivery orders bypass us. Customers complain to FoodPanda first</p>
					<p><strong>Solution:</strong> Respond to FoodPanda messages within 1 hour. Include phone number in packaging for direct complaints.</p>
