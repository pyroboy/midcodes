		<section class="content-block">
			<h2>20.3 Delivery-Only Menu Strategy</h2>
			<p>We don't list our entire menu on FoodPanda. Strategic curation protects margins and guides customers to profitable items.</p>

			<div class="menu-strategy">
				<div class="strategy-section">
					<h3>✅ Items to List on FoodPanda</h3>
					<ul class="menu-list">
						<li><strong>Shanghai Silog (₱140)</strong> - Highest margin, premium appeal</li>
						<li><strong>Burger Steak (₱125)</strong> - High COGS absorbs commission</li>
						<li><strong>Tapasya (₱130)</strong> - Premium, good for upselling</li>
						<li><strong>Combo Meals (₱165-180)</strong> - Multiple items = bigger margins</li>
						<li><strong>Lugaw w/ Toppings Bundle (₱120)</strong> - Lugaw + egg + meat = higher price point</li>
						<li><strong>Extra Rice (₱20)</strong> - High margin add-on</li>
						<li><strong>Extra Meat Upcharge (₱30-40)</strong> - Customer customization, pure margin</li>
					</ul>
				</div>

				<div class="strategy-section">
					<h3>❌ Items NOT to List</h3>
					<ul class="menu-list">
						<li><strong>Plain Longsilog (₱95)</strong> - 22% margin, too thin</li>
						<li><strong>Garlic Silog (₱100)</strong> - Below profitability threshold</li>
						<li><strong>Plain Lugaw (₱50)</strong> - 16% margin, impossible</li>
						<li><strong>Beverages</strong> - Heavy, low margin, fragile in delivery</li>
						<li><strong>One-off items</strong> - Focus on bestsellers to minimize complexity</li>
					</ul>
				</div>

				<div class="strategy-detail">
					<h4>Why This Matters</h4>
					<p>By limiting menu to 5-7 high-margin items, we:</p>
					<ul style="padding-left: 1.5rem;">
						<li>Simplify kitchen operations during delivery prep</li>
						<li>Reduce menu confusion for customers</li>
						<li>Maintain gross margins above 25% on all delivery sales</li>
						<li>Focus marketing on bestsellers (easier social media content)</li>
						<li>Protect brand if delivery service underperforms</li>
					</ul>
				</div>
			</div>
