		<section class="content-block">
			<h2>20.1 FoodPanda Commission Structure</h2>
			<p>FoodPanda is the primary delivery platform for Tagbilaran. Understanding the commission structure is critical to profitability analysis. We are strategic about which items we sell via delivery.</p>

			<div class="commission-box">
				<h3>FoodPanda Pricing Model</h3>
				<div class="commission-details">
					<div class="detail-item">
						<span class="label">Commission Rate</span>
						<span class="value">25-30%</span>
						<p>Typical rate for food delivery. Can vary by location & agreement. Expect 28% baseline initially.</p>
					</div>
					<div class="detail-item">
						<span class="label">Delivery Fee</span>
						<span class="value">₱30-50</span>
						<p>Customer pays this, not us. FoodPanda covers delivery logistics.</p>
					</div>
					<div class="detail-item">
						<span class="label">Payment Terms</span>
						<span class="value">Weekly Bank Transfer</span>
						<p>Payments settle every Tuesday for previous week's orders. 7-day cash delay on revenue.</p>
					</div>
					<div class="detail-item">
						<span class="label">Minimum Order</span>
						<span class="value">₱150</span>
						<p>Customers must order minimum amount. Helps basket size but filters low-value orders.</p>
					</div>
				</div>
			</div>

			<div class="highlight-box">
				<h3>⚠️ Commission Impact</h3>
				<p>A ₱100 menu item marked up to ₱125 on FoodPanda means we receive only <strong>₱90</strong> (₱125 - ₱35 commission). Our margin drops significantly. <strong>This is why we must be selective about which items we list.</strong></p>
			</div>
