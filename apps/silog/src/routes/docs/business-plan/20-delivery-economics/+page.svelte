		<!-- 20.4 Pricing Strategy -->
		<section class="content-block">
			<h2>20.4 Delivery Pricing Rules</h2>
			<p>Smart pricing on FoodPanda is the difference between profit and loss. Here are the rules we follow.</p>

			<div class="pricing-rules">
				<div class="rule-card">
					<h3>Rule 1: Add 15-25% Markup</h3>
					<p><strong>Walk-in price:</strong> ₱100</p>
					<p><strong>FoodPanda price:</strong> ₱120-125</p>
					<p>The markup compensates for commission loss while remaining competitive. Customers expect slight premium for delivery convenience.</p>
				</div>

				<div class="rule-card">
					<h3>Rule 2: Never Go Below 28% Margin</h3>
					<p><strong>If markup gives us less than 28% profit margin, don't list it.</strong></p>
					<p>Example: ₱95 item. After 30% commission + ₱10 packaging, we can't hit 28% margin. Skip it.</p>
				</div>

				<div class="rule-card">
					<h3>Rule 3: Bundle for Value Perception</h3>
					<p><strong>Create "Delivery Exclusive" combos</strong></p>
					<ul>
						<li>"Breakfast Power Pack" - Silog + extra rice + egg (₱165)</li>
						<li>"Late Night Bundle" - Lugaw + toppings + meat (₱150)</li>
						<li>"Duo Meal" - 2 mini silog (₱140)</li>
					</ul>
					<p>Bundles feel like value while maintaining margin.</p>
				</div>

				<div class="rule-card">
					<h3>Rule 4: Premium Positioning</h3>
					<p>Price 15-20% above carinderia average on FoodPanda. This signals quality & justifies higher price to customers who expect delivery premium.</p>
					<p><strong>Carinderia silog:</strong> ₱75-85 | <strong>Our FoodPanda:</strong> ₱120-140</p>
				</div>

				<div class="rule-card">
					<h3>Rule 5: Offer Strategic Discounts</h3>
					<p>Use FoodPanda's "flash sale" feature for 2-3 hours during peak times (8-10 AM, 12-1 PM, 6-7 PM).</p>
					<p><strong>Discount: 10% off</strong> on meal bundles. Drives order volume without killing margins.</p>
				</div>

				<div class="rule-card">
					<h3>Rule 6: Adjust Quarterly</h3>
					<p>Review prices monthly. If FoodPanda commission increases or ingredient costs rise, adjust menu prices upward by 2-5%.</p>
				</div>
			</div>
		</section>
