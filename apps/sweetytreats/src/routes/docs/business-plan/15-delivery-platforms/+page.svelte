		<h2>15.6 Quality Control & Freshness Assurance</h2>
        <p>
			Delivery adds time between baking and consumption. Strict quality controls ensure cookies
			arrive fresh and delicious, protecting brand reputation.
		</p>

        <div class="highlight-box">
			<h3>🔍 Freshness Protocol</h3>
			<ul>
                <li><strong>Bake Schedule:</strong> Only bake for delivery orders that afternoon/evening</li>
                <li><strong>Cool Time:</strong> Let cookies cool completely to room temperature before packaging (prevents moisture buildup)</li>
                <li><strong>Humidity Check:</strong> Use silica gel packets in insulated bags during rainy season</li>
                <li><strong>Delivery Window:</strong> Only accept orders for next-day delivery to guarantee freshness</li>
                <li><strong>Temperature Test:</strong> Spot-check received packages to verify proper insulation</li>
                <li><strong>Customer Feedback:</strong> Actively encourage reviews and immediately address complaints</li>
                <li><strong>Replacement Policy:</strong> Quick replacement for any damaged items (shows commitment)</li>
            </ul>
		</div>
        <div class="divider"></div>

		<h2>15.7 Delivery Radius & Logistics for Tagbilaran</h2>
        <p>
			Tagbilaran's geography and traffic patterns affect delivery efficiency and cost.
		</p>

        <ul class="simple-list">
			<li><strong>Primary Zone (0-3km):</strong> Home delivery, local businesses — fast delivery, happy customers</li>
			<li><strong>Secondary Zone (3-8km):</strong> Suburbs, outlying barangays — ₱50-100 extra fee to cover delivery cost</li>
			<li><strong>Tertiary Zone (8km+):</strong> Provincial areas — only via Lalamove or bulk order minimum ₱800</li>
			<li><strong>Peak Hours:</strong> Lunch (11am-2pm) and evening (5-7pm) are highest demand — schedule baking accordingly</li>
			<li><strong>Traffic Seasons:</strong> School opening and holiday seasons see higher delivery volume</li>
			<li><strong>Ferry Considerations:</strong> Orders to other islands must be bulk or premium pricing to justify transport</li>
		</ul>
