		<h2>15.2 Commission Structure & Margin Impact</h2>
        <p>
			Delivery platform commissions significantly impact profitability. However, treats have
			higher margins than regular food items, making them more suitable for delivery partnerships.
		</p>

        <div class="margin-comparison">
            <div class="comparison-item">
                <h4>Traditional Restaurant Meal</h4>
                <ul>
                    <li>Avg Cost: ₱100-150</li>
                    <li>Selling Price: ₱250-300</li>
                    <li>Gross Margin: 40-50%</li>
                    <li>After 28% Commission: -20% margin</li>
                    <li>Result: Not viable for delivery</li>
                </ul>
            </div>
            <div class="comparison-item">
                <h4>SweetyTreats Premium Cookies</h4>
                <ul>
                    <li>Avg Cost: ₱30-50</li>
                    <li>Selling Price: ₱150-200</li>
                    <li>Gross Margin: 70-80%</li>
                    <li>After 28% Commission: 50-60% margin</li>
                    <li>Result: Highly profitable on delivery</li>
                </ul>
            </div>
        </div>

        <div class="highlight-box">
			<h3>💡 Margin Strategy</h3>
			<ul>
                <li><strong>Platform Pricing:</strong> Add 15-20% markup to delivery orders to absorb commission</li>
                <li><strong>Minimum Order:</strong> ₱300 minimum to ensure delivery profitability</li>
                <li><strong>Bundle Offers:</strong> Sell cookie assortments instead of individual items</li>
                <li><strong>Monthly Performance:</strong> Target 15% of total sales from delivery by Month 6</li>
            </ul>
		</div>
        <div class="divider"></div>

		<h2>15.3 Packaging for Delivery</h2>
        <p>
			Fresh cookies arriving safely is critical. Premium packaging protects product quality
			and creates a memorable unboxing experience that encourages repeat orders and reviews.
		</p>

        <ul class="simple-list">
			<li><strong>Insulated Bags:</strong> Thermal insulation bags (₱200-400 each) maintain freshness during transit, especially in Bohol's heat</li>
			<li><strong>Sturdy Boxes:</strong> Reinforced corrugated boxes prevent crushing. Double-boxing for larger orders</li>
			<li><strong>Internal Padding:</strong> Parchment paper or foam inserts separate cookies and prevent breakage</li>
			<li><strong>Freshness Seals:</strong> Use branded tape or stickers to signal care and authenticity</li>
			<li><strong>Thank You Cards:</strong> Small handwritten notes increase customer delight and encourage social sharing</li>
			<li><strong>QR Code:</strong> Link to Instagram or review platforms in packaging</li>
		</ul>
        <div class="divider"></div>

		<h2>15.4 Delivery-Optimized Product Selection</h2>
        <p>
			Not all cookies ship equally well. Choose products based on durability and customer demand
			in delivery channels.
		</p>

        <div class="shipping-guide">
            <div class="ships-well">
                <h4>✅ Ships Well (Shelf Stable)</h4>
                <ul>
                    <li>Chocolate chip cookies (classic, durable)</li>
                    <li>Sugar cookies (sturdy, pretty presentation)</li>
                    <li>Peanut butter cookies (dense, stable)</li>
                    <li>Oatmeal cookies (hearty, maintains texture)</li>
                    <li>Macadamia nut cookies (premium, high margin)</li>
                    <li>Coffee biscuits (crispy, long shelf life)</li>
                </ul>
            </div>
            <div class="ships-poorly">
                <h4>❌ Avoid Delivery</h4>
                <ul>
                    <li>Macarons (delicate, humidity-sensitive)</li>
                    <li>Cream-filled pastries (temperature-sensitive)</li>
                    <li>Brownies with fresh frosting (gets crushed easily)</li>
                    <li>Meringues (extremely fragile, breaks in transit)</li>
                </ul>
            </div>
        </div>
