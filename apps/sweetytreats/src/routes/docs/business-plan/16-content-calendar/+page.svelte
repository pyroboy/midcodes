		<h2>16.6 User-Generated Content Strategy</h2>
        <p>
			Your customers are your best marketers. Encourage them to share and feature
			their content to build authentic, cost-free marketing.
		</p>

        <div class="ugc-strategy">
            <div class="ugc-step">
                <span class="number">1</span>
                <h4>Make It Easy to Share</h4>
                <ul>
                    <li>Include branded hashtag in packaging: #SweetyTreats</li>
                    <li>QR code linking to Instagram tag page</li>
                    <li>Encourage photos with "Tag us for a chance to be featured!"</li>
                    <li>Follow customers who tag you, like their posts</li>
                </ul>
            </div>

            <div class="ugc-step">
                <span class="number">2</span>
                <h4>Incentivize Sharing</h4>
                <ul>
                    <li>Feature a customer weekly on your main feed</li>
                    <li>Monthly raffle: Share a post → enter to win free cookies</li>
                    <li>Loyalty reward: 5 tagged posts = ₱100 discount</li>
                    <li>Surprise & delight: Reply to all customer posts within 24 hours</li>
                </ul>
            </div>

            <div class="ugc-step">
                <span class="number">3</span>
                <h4>Repost & Celebrate</h4>
                <ul>
                    <li>Screenshot customer posts, repost to your Stories</li>
                    <li>Tag them, thank them publicly</li>
                    <li>Write genuine, personal comments on their photos</li>
                    <li>Build relationships that turn customers into brand ambassadors</li>
                </ul>
            </div>

            <div class="ugc-step">
                <span class="number">4</span>
                <h4>Create a Community</h4>
                <ul>
                    <li>Feature the most active community members</li>
                    <li>Create a "SweetyTreats Hall of Fame" highlight</li>
                    <li>Host monthly contests (best unboxing photo, etc.)</li>
                    <li>Turn customers into brand advocates</li>
                </ul>
            </div>
