		<h2>16.3 Photography & Video Tips for Food</h2>
        <p>
			Professional-looking food content is non-negotiable. Quality visuals drive orders.
			You don't need expensive equipment — just smart techniques.
		</p>

        <div class="tips-grid">
            <div class="tip-card">
                <h4>📸 Photography</h4>
                <ul>
                    <li><strong>Natural Light Only:</strong> Shoot by window, not under indoor lights</li>
                    <li><strong>Golden Hour:</strong> Best light is 1-2 hours before sunset</li>
                    <li><strong>Props:</strong> Rustic wooden boards, fresh flowers, napkins for context</li>
                    <li><strong>Angles:</strong> 45° angle shows texture; flat lay shows variety</li>
                    <li><strong>Close-Ups:</strong> Macro shots of chocolate chips, cookie texture</li>
                    <li><strong>Clean Background:</strong> Simple white cloth or marble prevents distraction</li>
                    <li><strong>Spacing:</strong> Room around subject — don't crowd the frame</li>
                </ul>
            </div>

            <div class="tip-card">
                <h4>🎥 Video Content</h4>
                <ul>
                    <li><strong>Slow Motion:</strong> Cookie breaking, chocolate dripping (satisfying)</li>
                    <li><strong>Time-Lapse:</strong> Baking process, dough rising (show progress)</li>
                    <li><strong>Sound:</strong> Crunching sound of cookie, mixer whirring (ASMR appeal)</li>
                    <li><strong>Text Overlays:</strong> "Step 1, Step 2" guides viewers through process</li>
                    <li><strong>Trending Audio:</strong> Use popular TikTok/Reels sounds, adapt to cookies</li>
                    <li><strong>Quick Cuts:</strong> 2-3 second clips keep pace, hold attention</li>
                    <li><strong>Hook Early:</strong> First 1 second determines if viewers stay</li>
                </ul>
            </div>
        </div>
