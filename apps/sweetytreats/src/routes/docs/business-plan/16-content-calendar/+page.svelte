		<h2>16.7 Budget & Paid Promotion</h2>
        <p>
			Social media success starts organic, but strategic paid boosts accelerate growth.
			SweetyTreats operates lean, focusing on organic first.
		</p>

        <div class="budget-breakdown">
            <div class="budget-item">
                <h4>Organic (Primary Strategy)</h4>
                <p><strong>Cost: ₱0</strong></p>
                <ul>
                    <li>Consistent daily posting (7 days/week)</li>
                    <li>Engage with community (like, comment, reply)</li>
                    <li>Repost customer content</li>
                    <li>Use trending sounds and hashtags</li>
                    <li>Live streams for engagement</li>
                </ul>
            </div>

            <div class="budget-item">
                <h4>Paid Boosts (Occasional)</h4>
                <p><strong>Cost: ₱300-500/month</strong></p>
                <ul>
                    <li>Boost top-performing posts (₱100-150 each)</li>
                    <li>Instagram Ads: 3-4 posts/month (target Tagbilaran, ages 18-45)</li>
                    <li>Facebook Ads: 2-3 posts/month (wider age range, local targeting)</li>
                    <li>TikTok Ads: Optional, test with ₱200 budget when ready to scale</li>
                    <li>Best ROI: Boost posts already getting engagement organically</li>
                </ul>
            </div>

            <div class="budget-item">
                <h4>Timeline for Paid Spend</h4>
                <ul>
                    <li><strong>Month 1-2:</strong> ₱0 — Build organic following</li>
                    <li><strong>Month 3-4:</strong> Start with ₱200-300/month on best posts</li>
                    <li><strong>Month 5-6:</strong> Increase to ₱400-500/month as revenue allows</li>
                </ul>
            </div>
        </div>
