		<h2>16.2 Content by Platform</h2>
        <p>
			Different platforms have different audiences and content preferences. Tailor content
			while maintaining consistent branding across all channels.
		</p>

        <div class="platform-content">
            <div class="platform">
                <span class="platform-icon">📸</span>
                <h4>Instagram</h4>
                <p><strong>Best for:</strong> Visual storytelling, younger audience (18-35), trending features</p>
                <ul class="platform-list">
                    <li>High-quality photos (3000px minimum)</li>
                    <li>Use 5-10 relevant hashtags per post</li>
                    <li>Stories daily — behind-the-scenes, polls, Q&A</li>
                    <li>Reels (15-60 sec video) — baking, unboxing, trends</li>
                    <li>Captions: friendly, storytelling, CTA</li>
                    <li>Posting time: 7-9pm (peak engagement)</li>
                </ul>
            </div>

            <div class="platform">
                <span class="platform-icon">👥</span>
                <h4>Facebook</h4>
                <p><strong>Best for:</strong> Community, older audience (30+), direct orders, groups</p>
                <ul class="platform-list">
                    <li>Mix of photos and longer-form text posts</li>
                    <li>Create a "SweetyTreats Community" group for loyal customers</li>
                    <li>Share longer stories (2-3 paragraphs)</li>
                    <li>Live videos (10-30 min) — baking, Q&A</li>
                    <li>Engagement is key — respond to comments within 1 hour</li>
                    <li>Posting time: 1-3pm, 7-9pm (peak for this demographic)</li>
                </ul>
            </div>

            <div class="platform">
                <span class="platform-icon">🎵</span>
                <h4>TikTok</h4>
                <p><strong>Best for:</strong> Viral reach, younger audience (13-25), trends, entertainment</p>
                <ul class="platform-list">
                    <li>Short, snappy videos (15-60 seconds)</li>
                    <li>Hook viewers in first 3 seconds</li>
                    <li>Trending sounds & challenges (adapt them to cookies)</li>
                    <li>Show personality — be playful, authentic</li>
                    <li>Text overlays, quick cuts, transitions</li>
                    <li>Posting time: 6-10am, 7-11pm (peak for younger audience)</li>
                    <li>Post 3-5x per week for growth</li>
                </ul>
            </div>
