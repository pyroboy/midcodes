		<h2>3.1 Industry Overview (ID Cards + EdTech)</h2>

		<p>
			The Philippine identity card market is experiencing a fundamental shift from traditional paper-based
			laminated IDs to durable, tech-enabled smart cards. This transformation is driven by three key forces:
		</p>

		<div class="industry-drivers">
			<div class="driver-card">
				<span class="driver-icon">üì±</span>
				<h4>Digital Transformation</h4>
				<p>
					Schools, LGUs, and businesses are adopting QR-based verification to combat counterfeiting.
					The shift from analog logbooks to digital attendance systems creates demand for smart IDs.
				</p>
			</div>
			<div class="driver-card">
				<span class="driver-icon">üîê</span>
				<h4>Security Concerns</h4>
				<p>
					Rising cases of fake PWD/Senior IDs for discount fraud. Government mandates (RA 7277, RA 9994)
					require verifiable identification. High-profile incidents drive institutional spending.
				</p>
			</div>
			<div class="driver-card">
				<span class="driver-icon">üë®‚Äçüë©‚Äçüëß</span>
				<h4>Parental Demand</h4>
				<p>
					Post-pandemic safety consciousness. Parents willing to pay for attendance tracking with SMS
					notifications. Schools face competitive pressure to offer "smart campus" features.
				</p>
			</div>
		</div>

