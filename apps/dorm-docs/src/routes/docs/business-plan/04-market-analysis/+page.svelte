		<h2>Target Market</h2>
		<p>Our primary target market consists of students and young professionals who require affordable, safe, and convenient housing in Tagbilaran City.</p>
		<ul class="market-list">
			<li>
				<strong>University Students</strong>
				<p>Primarily from <strong>University of Bohol (UB)</strong> and other nearby institutions. These students prioritize proximity to campus, reliable internet for studies, and a study-friendly environment.</p>
			</li>
			<li>
				<strong>Review Center Enrollees</strong>
				<p>Students enrolled in review centers (e.g., JMC) preparing for board exams. They require quiet zones and strict house rules to ensure focus.</p>
			</li>
			<li>
				<strong>Young Professionals (BPO)</strong>
				<p>Workers from nearby BPO firms like <strong>IBEX</strong> and <strong>Marcela</strong>. They often work night shifts and value security, daytime quietness, and stable high-speed internet for WFH setups.</p>
			</li>
		</ul>
